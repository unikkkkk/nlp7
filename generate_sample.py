def generate_sample(output_path: Path, add_noise: bool = True) -> None:
    import librosa
    from gtts import gTTS

    mp3_path = output_path.with_suffix(".mp3")

    print("[INFO] Generating Ukrainian sample audio via gTTS...")
    tts = gTTS(text=SAMPLE_TEXT.strip(), lang="uk", slow=False)
    tts.save(str(mp3_path))

    audio, sr = librosa.load(str(mp3_path), sr=16000, mono=True)

    if add_noise:
        # SNR = 22 dB moderate noise for demo
        signal_power = np.mean(audio ** 2)
        noise_power  = signal_power / (10 ** (22 / 10))
        noise = np.random.normal(0, np.sqrt(noise_power), len(audio)).astype(np.float32)
        audio = np.clip(audio + noise, -1.0, 1.0).astype(np.float32)

    sf.write(str(output_path), audio, sr)
    mp3_path.unlink(missing_ok=True)
    print(f"[OK] Sample audio: {output_path} — {len(audio)/sr:.1f}s, {len(SAMPLE_TEXT.split())} words in source text")


if __name__ == "__main__":
    from config import SAMPLE_AUDIO
    generate_sample(SAMPLE_AUDIO)
